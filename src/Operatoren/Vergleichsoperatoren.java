package Operatoren;

public class Vergleichsoperatoren {
    public static void main(String[] args) {
        //==,<,>,<=,>=,!=:
        int a = 1;
        int b = 1;
        if(a == b){
            System.out.println("a = b");
        }
        else{
            System.out.println("a != b");
        }
    }
}